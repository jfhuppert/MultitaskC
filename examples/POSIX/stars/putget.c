#include "putget.h"

extern int printf(const char *format, ...);

/*------------------------------------------------------------------------------*/
void putdec(long number)
{
   printf("%ld",number);
}
/*------------------------------------------------------------------------------*/
void puthex(unsigned long v)
{
   printf("%lx",v);
}
/*------------------------------------------------------------------------------*/
void putstr(char *s)
{
   while(*s) putchar(*s++);
}
